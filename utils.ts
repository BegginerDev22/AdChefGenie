export const scaleAmount = (amountStr: string, base: number, target: number): string => {
  if (!base || base === target) return amountStr;
  
  const factor = target / base;
  const regex = /^(\d+[\s-]+\d+\/\d+|\d+\/\d+|\d+(?:\.\d+)?)\s*(.*)$/;
  const match = amountStr.match(regex);
  
  if (!match) return amountStr;
  
  let numStr = match[1];
  const unitStr = match[2];
  let value = 0;
  
  if (numStr.includes('/')) {
     if (numStr.includes(' ') || numStr.includes('-')) {
        const parts = numStr.split(/[\s-]+/);
        const frac = parts[1].split('/');
        value = parseInt(parts[0]) + parseInt(frac[0]) / parseInt(frac[1]);
     } else {
        const frac = numStr.split('/');
        value = parseInt(frac[0]) / parseInt(frac[1]);
     }
  } else {
     value = parseFloat(numStr);
  }
  
  if (isNaN(value)) return amountStr;
  
  const newValue = value * factor;
  return `${formatNumber(newValue)}${unitStr ? ' ' + unitStr : ''}`;
};

export const getCuisineColor = (cuisine?: string): string => {
  if (!cuisine) return 'bg-gray-600';
  const c = cuisine.toLowerCase();
  if (c.includes('indian')) return 'bg-orange-600';
  if (c.includes('italian')) return 'bg-red-600';
  if (c.includes('mexican')) return 'bg-green-600';
  if (c.includes('chinese') || c.includes('asian')) return 'bg-amber-500';
  if (c.includes('thai')) return 'bg-emerald-500';
  if (c.includes('japanese')) return 'bg-rose-500';
  if (c.includes('mediterranean') || c.includes('greek')) return 'bg-cyan-600';
  if (c.includes('french')) return 'bg-sky-600';
  if (c.includes('american')) return 'bg-blue-600';
  return 'bg-slate-600';
};

