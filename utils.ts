export const shareRecipe = async (text: string, title: string, onCopy?: () => void, url?: string) => {
    if (navigator.share) {
        try {
            await navigator.share({
                title: title,
                text: text,
                url: url
            });
        } catch (err) {
            console.log("Share skipped or failed", err);
        }
    } else {
        try {
            const textToCopy = url ? `${text}\n\nLink: ${url}` : text;
            await navigator.clipboard.writeText(textToCopy);
            if (onCopy) onCopy();
        } catch (err) {
            console.error("Clipboard failed", err);
        }
    }
}
